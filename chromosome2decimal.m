function [decimal] = chromosome2decimal(chromosome)
    total = chromosome(1)*2^0 + ...
            chromosome(2)*2^1 + ...
            chromosome(3)*2^2 + ...
            chromosome(4)*2^3 + ...
            chromosome(5)*2^4 + ...
            chromosome(6)*2^5 + ...
            chromosome(7)*2^6 + ...
            chromosome(8)*2^7 + ...
            chromosome(9)*2^8 + ...
            chromosome(10)*2^9 + ...
            chromosome(11)*2^10 + ...
            chromosome(12)*2^11 + ...
            chromosome(13)*2^12 + ...
            chromosome(14)*2^13 + ...
            chromosome(15)*2^14 + ...
            chromosome(16)*2^15 + ...
            chromosome(17)*2^16 + ...
            chromosome(18)*2^17 + ...
            chromosome(19)*2^18 + ...
            chromosome(20)*2^19 + ...
            chromosome(21)*2^20;
    decimal = (1e3)/total;
end